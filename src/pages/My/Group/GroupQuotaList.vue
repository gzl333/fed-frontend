<template>
  <div class="GroupQuotaList">

    <div class="row items-center justify-between q-py-md">

      <div class="col-3">
        <div class="row justify-start">
          <div class="col">
            <q-input dense outlined v-model="search">
              <template v-slot:prepend>
                <q-icon name="search"/>
              </template>
              <template v-slot:append>
                <q-icon name="close" @click="search = ''" class="cursor-pointer" />
              </template>
            </q-input>
          </div>
        </div>
      </div>

      <div class="col-2">
        <div class="row justify-end">
          <div class="col">
            <q-select outlined dense stack-label label="筛选" v-model="filterSelection"
                      :options="filterOptions"/>
          </div>
        </div>
      </div>

    </div>

    <quota-table :quotas="quotas" :search="search.trim().toLowerCase()" is-group/>

  </div>
</template>

<script lang="ts">
import { computed, defineComponent, ref } from 'vue'
import QuotaTable from 'components/Quota/QuotaTable.vue'
import { useStore } from 'vuex'
import { StateInterface } from 'src/store'

export default defineComponent({
  name: 'GroupQuotaList',
  components: {
    QuotaTable
  },
  props: {},
  setup () {
    const $store = useStore<StateInterface>()

    // application filter
    const filterSelection = ref({
      label: '全部',
      value: '0'
    })

    const filterOptions = [
      {
        label: '全部',
        value: '0'
      },
      {
        label: '可用',
        value: 'valid'
      },
      {
        label: '不可用',
        value: 'invalid'
      }
    ]

    const quotas = computed(() => $store.getters['server/getGroupQuotasByFilter'](filterSelection.value.value))
    // 搜索框
    const search = ref('')

    return {
      quotas,
      filterSelection,
      filterOptions,
      search
    }
  }
})
</script>

